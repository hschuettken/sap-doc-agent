def generate_technical_doc(data: dict) -> dict:
    """Return technical documentation as a markdown string.

    Covers all technical_objects (name, type, platform, layer, status, SQL)
    and deployment ordering from tech_specs.

    Returns:
        {title, content (markdown), deployment_order (list[str]), object_count (int)}
    """
    project_name = _project_name(data)
    objects = _all_tech_objects(data)
    deployment_order = _deployment_order(data)

    lines: list[str] = [
        f"# Technical Documentation — {project_name}",
        "",
        "## Technical Objects",
        "",
    ]

    if not objects:
        lines.append("_No technical objects defined._")
    else:
        for obj in objects:
            name = obj.get("name", "unnamed")
            otype = obj.get("object_type", "—")
            platform = obj.get("platform", "—")
            layer = obj.get("layer", "—")
            status = obj.get("status", "—")
            artifact = obj.get("generated_artifact", "")

            lines.append(f"### `{name}`")
            lines.append("")
            lines.append(f"- **Type:** {otype}")
            lines.append(f"- **Platform:** {platform}")
            lines.append(f"- **Layer:** {layer}")
            lines.append(f"- **Status:** {status}")
            if artifact:
                lines.append("")
                lines.append("**Generated Artifact:**")
                lines.append("")
                lines.append("```sql")
                lines.append(artifact)
                lines.append("```")
            lines.append("")

    if deployment_order:
        lines.append("## Deployment Order")
        lines.append("")
        for i, item in enumerate(deployment_order, 1):
            lines.append(f"{i}. `{item}`")
        lines.append("")

    return {
        "title": f"Technical Documentation — {project_name}",
        "content": "\n".join(lines),
        "deployment_order": deployment_order,
        "object_count": len(objects),
    }


def generate_functional_doc(data: dict) -> dict:
    """Return functional documentation as a markdown string.

    Covers requirements with business domain, KPIs (name + formula), parsed entities.

    Returns:
        {title, content (markdown)}
    """
    project_name = _project_name(data)
    requirements = data.get("requirements", [])

    lines: list[str] = [
        f"# Functional Documentation — {project_name}",
        "",
        "## Requirements",
        "",
    ]

    if not requirements:
        lines.append("_No requirements defined._")
    else:
        for req in requirements:
            req_id = req.get("id", "—")
            title = req.get("title", "Untitled")
            domain = req.get("business_domain", "—")
            status = req.get("status", "—")

            lines.append(f"### {title}")
            lines.append("")
            lines.append(f"- **ID:** {req_id}")
            lines.append(f"- **Business Domain:** {domain}")
            lines.append(f"- **Status:** {status}")

            # KPIs
            kpis = _parse_jsonb(req.get("parsed_kpis", []))
            if isinstance(kpis, list) and kpis:
                lines.append("")
                lines.append("**KPIs:**")
                lines.append("")
                for kpi in kpis:
                    if not isinstance(kpi, dict):
                        continue
                    kpi_name = kpi.get("name", "unnamed")
                    formula = kpi.get("formula", "")
                    if formula:
                        lines.append(f"- **{kpi_name}**: `{formula}`")
                    else:
                        lines.append(f"- **{kpi_name}**")

            # Parsed entities
            entities = _parse_jsonb(req.get("parsed_entities", {}))
            if entities:
                lines.append("")
                lines.append("**Entities:**")
                lines.append("")
                for entity_type, entity_list in entities.items():
                    if isinstance(entity_list, list):
                        names = ", ".join(str(e) for e in entity_list)
                    else:
                        names = str(entity_list)
                    lines.append(f"- **{entity_type}:** {names}")

            lines.append("")

    return {
        "title": f"Functional Documentation — {project_name}",
        "content": "\n".join(lines),
    }


def generate_traceability_matrix(data: dict) -> dict:
    """Build a traceability matrix linking requirements to HLA decisions,
    technical objects, test cases, and reconciliation results.

    Returns:
        {rows: list[{requirement, hla_decision, tech_objects, test_cases, result, result_class}]}
    """
    requirements = data.get("requirements", [])
    hla_documents = data.get("hla_documents", [])
    tech_specs = data.get("tech_specs", [])
    reconciliation_results = data.get("reconciliation_results", [])
    technical_objects = _all_tech_objects(data)

    # Build lookup: first hla narrative (simplification — real traceability would use IDs)
    hla_narrative = hla_documents[0].get("narrative", "—") if hla_documents else "—"

    # Collect all object names
    all_obj_names = [obj.get("name", "") for obj in technical_objects if obj.get("name")]

    # Collect all test case keys
    all_test_keys = [r.get("test_case_key", "") for r in reconciliation_results if r.get("test_case_key")]

    # Determine aggregate result
    def _aggregate_result(rec_results: list[dict]) -> tuple[str, str]:
        if not rec_results:
            return "no tests", "draft"
        statuses = [r.get("delta_status", "unknown") for r in rec_results]
        if any(s == "fail" for s in statuses):
            return "fail", "fail"
        if any(s == "tolerance" for s in statuses):
            return "tolerance", "tolerance"
        return "pass", "pass"

    rows: list[dict] = []

    if not requirements:
        # At minimum one row from whatever we have
        result, result_class = _aggregate_result(reconciliation_results)
        rows.append(
            {
                "requirement": "—",
                "hla_decision": hla_narrative[:80] if hla_narrative else "—",
                "tech_objects": all_obj_names[:5],
                "test_cases": all_test_keys[:5],
                "result": result,
                "result_class": result_class,
            }
        )
    else:
        for req in requirements:
            title = req.get("title", req.get("id", "—"))
            result, result_class = _aggregate_result(reconciliation_results)
            rows.append(
                {
                    "requirement": title,
                    "hla_decision": hla_narrative[:80] if hla_narrative else "—",
                    "tech_objects": all_obj_names,
                    "test_cases": all_test_keys,
                    "result": result,
                    "result_class": result_class,
                }
            )

    return {"rows": rows}


def generate_decision_log(data: dict) -> list[dict]:
    """Extract architecture decisions.

    Returns:
        list of {topic, choice, rationale, alternatives, platform_placement}
    """
    decisions = data.get("architecture_decisions", [])
    log: list[dict] = []
    for dec in decisions:
        log.append(
            {
                "topic": dec.get("topic", "—"),
                "choice": dec.get("choice", "—"),
                "rationale": dec.get("rationale", "—"),
                "alternatives": dec.get("alternatives", []),
                "platform_placement": dec.get("platform_placement", "—"),
            }
        )
    return log


def generate_reconciliation_report(data: dict) -> dict:
    """Summarise reconciliation results.

    Returns:
        {total_tests, passed, tolerance, expected_change, failed, results}
    """
    results = data.get("reconciliation_results", [])
    passed = 0
    tolerance = 0
    failed = 0
    expected_change = 0

    for r in results:
        status = r.get("delta_status", "").lower()
        if status == "pass":
            passed += 1
        elif status == "tolerance":
            tolerance += 1
        elif status == "fail":
            failed += 1
        elif status == "expected_change":
            expected_change += 1

    return {
        "total_tests": len(results),
        "passed": passed,
        "tolerance": tolerance,
        "expected_change": expected_change,
        "failed": failed,
        "results": results,
    }


