def _md_to_simple_html(md: str) -> str:
    """Minimal markdown-to-HTML converter for inline rendering.

    Handles: h1-h3, bold, code spans, code blocks, bullet lists, line breaks.
    """
    lines = md.split("\n")
    html_lines: list[str] = []
    in_code_block = False
    in_list = False

    for line in lines:
        # Code block toggle
        if line.strip().startswith("```"):
            if in_list:
                html_lines.append("</ul>")
                in_list = False
            if in_code_block:
                html_lines.append("</code></pre>")
                in_code_block = False
            else:
                lang = line.strip()[3:].strip() or ""
                html_lines.append(f'<pre class="sql-block"><code class="{lang}">')
                in_code_block = True
            continue

        if in_code_block:
            html_lines.append(line)
            continue

        # Headings
        if line.startswith("### "):
            if in_list:
                html_lines.append("</ul>")
                in_list = False
            content = line[4:].strip()
            html_lines.append(f"<h3>{content}</h3>")
            continue
        if line.startswith("## "):
            if in_list:
                html_lines.append("</ul>")
                in_list = False
            content = line[3:].strip()
            html_lines.append(f"<h2>{content}</h2>")
            continue
        if line.startswith("# "):
            if in_list:
                html_lines.append("</ul>")
                in_list = False
            content = line[2:].strip()
            html_lines.append(f"<h1>{content}</h1>")
            continue

        # Bullet list
        if line.startswith("- "):
            if not in_list:
                html_lines.append("<ul>")
                in_list = True
            content = line[2:]
            content = _inline_md(content)
            html_lines.append(f"<li>{content}</li>")
            continue

        # Numbered list
        num_match = re.match(r"^(\d+)\. (.*)", line)
        if num_match:
            if in_list:
                html_lines.append("</ul>")
                in_list = False
            content = _inline_md(num_match.group(2))
            html_lines.append(f"<p>{num_match.group(1)}. {content}</p>")
            continue

        # Close list if needed
        if in_list and line.strip() == "":
            html_lines.append("</ul>")
            in_list = False

        if line.strip() == "":
            html_lines.append("<br>")
        else:
            content = _inline_md(line)
            html_lines.append(f"<p>{content}</p>")

    if in_list:
        html_lines.append("</ul>")
    if in_code_block:
        html_lines.append("</code></pre>")

    return "\n".join(html_lines)


def _inline_md(text: str) -> str:
    """Convert inline markdown (bold, code) to HTML."""
    # Bold
    text = re.sub(r"\*\*(.+?)\*\*", r"<strong>\1</strong>", text)
    # Code span
    text = re.sub(r"`([^`]+)`", r"<code>\1</code>", text)
    return text


def render_html_report(data: dict) -> str:
    """Render full as-built report as self-contained HTML.

    Uses the Jinja2 template at _TEMPLATE_PATH; falls back to _FALLBACK_TEMPLATE
    if the file isn't found.

    Returns:
        HTML string
    """
    from jinja2 import Template  # noqa: PLC0415

    project = data.get("project", {})
    customer = data.get("customer", {})
    project_name = project.get("name", "Unknown Project")
    customer_name = customer.get("name", "Unknown Customer")
    generated_at = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    # Assemble sub-documents
    tech_doc = generate_technical_doc(data)
    func_doc = generate_functional_doc(data)
    recon_report = generate_reconciliation_report(data)
    traceability = generate_traceability_matrix(data)
    decision_log = generate_decision_log(data)

    technical_objects = _all_tech_objects(data)
    requirements = data.get("requirements", [])

    # Summary cards
    summary_cards = [
        {"value": len(requirements), "label": "Requirements"},
        {"value": tech_doc["object_count"], "label": "Technical Objects"},
        {"value": recon_report["total_tests"], "label": "Test Cases"},
        {"value": recon_report["passed"], "label": "Tests Passed"},
    ]

    # Sections
    sections = [
        {
            "title": "Functional Overview",
            "html_content": _md_to_simple_html(func_doc["content"]),
        },
        {
            "title": "Technical Specification",
            "html_content": _md_to_simple_html(tech_doc["content"]),
        },
    ]

    reconciliation = {
        "total_tests": recon_report["total_tests"],
        "passed": recon_report["passed"],
        "tolerance": recon_report["tolerance"],
        "failed": recon_report["failed"],
    }

    template_src: str
    if _TEMPLATE_PATH.exists():
        template_src = _TEMPLATE_PATH.read_text()
    else:
        template_src = _FALLBACK_TEMPLATE

    template = Template(template_src)
    return template.render(
        title=f"As-Built Documentation — {project_name}",
        subtitle=f"Customer: {customer_name}",
        customer_name=customer_name,
        project_name=project_name,
        generated_at=generated_at,
        summary_cards=summary_cards,
        sections=sections,
        traceability_rows=traceability["rows"],
        decision_log=decision_log,
        reconciliation=reconciliation,
        technical_objects=technical_objects,
    )


def render_markdown_report(data: dict) -> str:
    """Render a full as-built report as Markdown.

    Combines functional doc, technical doc, traceability table, decision log,
    and reconciliation summary into a single markdown string.

    Returns:
        Markdown string
    """
    project_name = _project_name(data)
    customer_name = _customer_name(data)
    generated_at = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    tech_doc = generate_technical_doc(data)
    func_doc = generate_functional_doc(data)
    recon_report = generate_reconciliation_report(data)
    traceability = generate_traceability_matrix(data)
    decision_log = generate_decision_log(data)

    lines: list[str] = [
        f"# As-Built Documentation — {project_name}",
        "",
        f"**Customer:** {customer_name}  ",
        f"**Generated:** {generated_at}",
        "",
        "---",
        "",
        func_doc["content"],
        "",
        "---",
        "",
        tech_doc["content"],
        "",
        "---",
        "",
        "## Traceability Matrix",
        "",
        "| Requirement | HLA Decision | Tech Objects | Test Cases | Result |",
        "|---|---|---|---|---|",
    ]

    for row in traceability["rows"]:
        req = row["requirement"]
        hla = row["hla_decision"]
        objs = ", ".join(row["tech_objects"]) if row["tech_objects"] else "—"
        tcs = ", ".join(row["test_cases"]) if row["test_cases"] else "—"
        result = row["result"]
        lines.append(f"| {req} | {hla} | {objs} | {tcs} | {result} |")

    lines.extend(
        [
            "",
            "---",
            "",
            "## Architecture Decision Log",
            "",
        ]
    )

    if decision_log:
        lines.extend(
            [
                "| Topic | Choice | Rationale | Platform |",
                "|---|---|---|---|",
            ]
        )
        for dec in decision_log:
            topic = dec["topic"]
            choice = dec["choice"]
            rationale = dec["rationale"]
            platform = dec["platform_placement"]
            lines.append(f"| {topic} | {choice} | {rationale} | {platform} |")
    else:
        lines.append("_No architecture decisions recorded._")

    lines.extend(
        [
            "",
            "---",
            "",
            "## Reconciliation Summary",
            "",
            f"- **Total Tests:** {recon_report['total_tests']}",
            f"- **Passed:** {recon_report['passed']}",
            f"- **Within Tolerance:** {recon_report['tolerance']}",
            f"- **Failed:** {recon_report['failed']}",
            "",
        ]
    )

    return "\n".join(lines)


def render_pdf_report(data: dict) -> bytes:
    """Render the as-built report as PDF bytes.

    Uses weasyprint to convert the HTML report to PDF.
    Falls back to returning the HTML as bytes if weasyprint is not installed.

    Returns:
        PDF bytes (or HTML bytes if weasyprint unavailable)
    """
    html = render_html_report(data)
    try:
        from weasyprint import HTML  # noqa: PLC0415

        return HTML(string=html).write_pdf()
    except ImportError:
        return html.encode("utf-8")


import logging as _logging  # noqa: E402

logger = _logging.getLogger(__name__)


async def sync_to_doc_platform(
    platform_type: str,
    html_content: str,
    title: str,
    space_name: str = "Spec2Sphere",
) -> dict:
    """Push generated documentation to a doc platform (BookStack/Confluence/Outline).

    Returns: {synced: bool, url: str|None, error: str|None}
    """
    try:
        import os as _os

        if platform_type == "bookstack":
            from spec2sphere.doc_platform.bookstack import BookStackAdapter  # noqa: PLC0415

            adapter = BookStackAdapter(
                base_url=_os.environ.get("BOOKSTACK_URL", ""),
                token_id=_os.environ.get("BOOKSTACK_TOKEN_ID", ""),
                token_secret=_os.environ.get("BOOKSTACK_TOKEN_SECRET", ""),
            )
        elif platform_type == "confluence":
            from spec2sphere.doc_platform.confluence import ConfluenceAdapter  # noqa: PLC0415

            adapter = ConfluenceAdapter(
                url=_os.environ.get("CONFLUENCE_URL", ""),
                token=_os.environ.get("CONFLUENCE_TOKEN", ""),
            )
        elif platform_type == "outline":
            from spec2sphere.doc_platform.outline import OutlineAdapter  # noqa: PLC0415

            adapter = OutlineAdapter(
                base_url=_os.environ.get("OUTLINE_URL", ""),
                api_key=_os.environ.get("OUTLINE_API_KEY", ""),
            )
        else:
            return {"synced": False, "url": None, "error": f"Unknown platform: {platform_type}"}

        space = await adapter.create_space(space_name, "Auto-generated by Spec2Sphere")
        page = await adapter.create_page(space.id, title, html_content)
        return {"synced": True, "url": page.url, "error": None}
    except Exception as exc:
        logger.warning("Doc platform sync failed (%s): %s", platform_type, exc)
        return {"synced": False, "url": None, "error": str(exc)}
